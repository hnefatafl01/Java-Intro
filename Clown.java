public class Clown {
  public String nose;
  public int shoes;
  public long creepiness;

  public Clown(String noseColor, int shoeSize, long creepyLevel) {
    this.nose = noseColor;
    this.shoes = shoeSize;
    this.creepiness = creepyLevel;
  }



  public static String sing() {
    return "Clown is singing";
  }

}
